def media(nota1, nota2):
    resultadoMedia = (nota1 + nota2) / 2
    print('Nota 1: ', nota1)
    print('Nota 2: ', nota2)
    print('A média do aluno é: ', resultadoMedia)

nota1 = float(input ('Digite a primeira nota: '))
nota2 = float(input ('Digite a segunda número: '))

media(nota1, nota2)
    